/**
 * Generates `src/constants/suppliers.ts` from the suppliers barrel.
 *
 * Why this exists: UI that only needs supplier *names* (the settings toggle list)
 * would otherwise call `SupplierFactory.supplierList()`, whose module does
 * `import * as suppliers from "."` — dragging all 28 supplier implementations and
 * their zod/fuzzball/liqe/jszip dependencies into whichever entry point imports it.
 * That put the whole supplier layer into the chunk shared by the popup and the
 * options page. Generating a dependency-free constant keeps the names available
 * without the implementations.
 *
 * The output is committed so tests and dev runs work without a build step; a unit
 * test re-checks it against the barrel in case someone edits the barrel without
 * regenerating.
 *
 * @module generate-supplier-constants
 */

import fs from "fs/promises";
import path, { dirname } from "path";
import { fileURLToPath } from "url";
import { getSupplierClassNames } from "./supplierList.js";

const _dirname = dirname(fileURLToPath(import.meta.url));
const OUT_PATH = path.resolve(_dirname, "../src/constants/suppliers.ts");

const _y = (text) => `\x1b[33m${text}\x1b[0m`;
const _c = (text) => `\x1b[36m${text}\x1b[0m`;

const classNames = await getSupplierClassNames();

if (classNames.length === 0) {
  // A regex that silently matched nothing would emit an empty list and quietly
  // blank the settings UI, so fail loudly instead.
  throw new Error(
    "No supplier exports found in src/suppliers/index.ts — refusing to write an empty list.",
  );
}

const contents = `// AUTO-GENERATED by tools/generate-supplier-constants.js — do not edit by hand.
// Regenerate with: pnpm run generate-supplier-constants

/**
 * The active supplier class names, mirroring the value exports of the
 * \`@/suppliers\` barrel.
 *
 * This module deliberately imports **nothing**. UI that only needs supplier
 * *names* reads them here instead of calling \`SupplierFactory.supplierList()\`,
 * which would pull every supplier implementation into the importing entry point's
 * bundle.
 * @category Constants
 * @group Suppliers
 * @source
 */
export const SUPPLIER_CLASS_NAMES = [
${classNames.map((name) => `  "${name}",`).join("\n")}
] as const satisfies ReadonlyArray<SupplierClassName>;
`;

await fs.writeFile(OUT_PATH, contents, "utf-8");
console.log(
  `Generated ${_c("src/constants/suppliers.ts")} with ${_y(classNames.length)} suppliers`,
);
